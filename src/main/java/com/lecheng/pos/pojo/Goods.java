package com.lecheng.pos.pojo;

public class Goods {
	private int goodId;
	private String goodName;
	private String goodSku;
	private double goodPrice;
	private String goodCategory;
	private String goodItem;
	private int goodDiscount;
	public int getGoodId() {
		return goodId;
	}
	public void setGoodId(int goodId) {
		this.goodId = goodId;
	}
	public String getGoodName() {
		return goodName;
	}
	public void setGoodName(String goodName) {
		this.goodName = goodName;
	}
	public String getGoodSku() {
		return goodSku;
	}
	public void setGoodSku(String goodSku) {
		this.goodSku = goodSku;
	}
	public double getGoodPrice() {
		return goodPrice;
	}
	public void setGoodPrice(double goodPrice) {
		this.goodPrice = goodPrice;
	}
	public String getGoodCategory() {
		return goodCategory;
	}
	public void setGoodCategory(String goodCategory) {
		this.goodCategory = goodCategory;
	}
	public String getGoodItem() {
		return goodItem;
	}
	public void setGoodItem(String goodItem) {
		this.goodItem = goodItem;
	}
	public int getGoodDiscount() {
		return goodDiscount;
	}
	public void setGoodDiscount(int goodDiscount) {
		this.goodDiscount = goodDiscount;
	}
	
}
